package com.nt.vo;

public class GuestVO {
	private Integer gno;
	private String gname;
	private String gadd;
	private String pdaycharge;
	private String daysstayed;
	private String bill;
	
	//setter and getter method 
	
	public Integer getGno() {
		return gno;
	}
	public void setGno(Integer gno) {
		this.gno = gno;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGadd() {
		return gadd;
	}
	public void setGadd(String gadd) {
		this.gadd = gadd;
	}
	public String getPdaycharge() {
		return pdaycharge;
	}
	public void setPdaycharge(String pdaycharge) {
		this.pdaycharge = pdaycharge;
	}
	public String getDaysstayed() {
		return daysstayed;
	}
	public void setDaysstayed(String daysstayed) {
		this.daysstayed = daysstayed;
	}
	public String getBill() {
		return bill;
	}
	public void setBill(String bill) {
		this.bill = bill;
	}
	
}
